// Função com parâmetro - quando a mensgaem muda
// Essa função utiliza o parâmetro para enviar a saudação
// Cada função tem um objetivo

function saudar (saudacao, nomeDousuario) {
    alert (saudacao + " " + nomeDousuario);
}

// Métodos
saudar ("Olá");
saudar ("Oi")
saudar ("Seja bem-vindo")

// Declaração (criando a função)
function cumprimentar () {
    // Código fica aqui
    alert ("Olá, tudo bem?");
}

// Invocação (chamar a função)
cumprimentar (); //Método
cumprimentar (); 
cumprimentar ();


