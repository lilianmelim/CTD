// adição
console.log(2 + 2);

// subtração
console.log(10 - 5);

// multiplicação
console.log(10*5);

// divisão
console.log(10/2);

// incremento
let numero = 10;
console.log(++numero);

//decremento
console.log(--numero);

//comparação maior
console.log(10 > 5);

//comparação menor
console.log(10 < 5);

// comparação maior igual
console.log(20 <= 19);

// comparação menor igual
console.log(20 >= 19);

// comparação de igualdade
console.log(10 == 10);

// comparação de igualdade rígido
console.log(5 === "5");

// comparação de diferente
console.log(10 != 10);

// comparação de diferente rígido
console.log(10 !== 20)

// comparador lógico &&
console.log(10 < 5 && 8 > 3)


// comparador lógico OR ||
console.log(10 < 5 || 8 > 3)
